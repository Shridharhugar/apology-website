// ==========================================
// Pause Current Video
// ==========================================

function stopCurrentVideo() {

    const videos = document.querySelectorAll("video");

    videos.forEach(video => {

        video.pause();

        video.currentTime = 0;

    });

}

// ==========================================
// Resume Background Music
// ==========================================

function resumeBackgroundMusic() {

    if (!bgMusic.paused) return;

    bgMusic.play()
        .then(() => {

            musicBtn.innerHTML = "⏸️";

            musicBtn.classList.add("playing");

        })
        .catch(() => {

            console.log("Autoplay blocked.");

        });

}

// ==========================================
// Create Image
// ==========================================

function createImage(memory) {

    return `
        <img
            src="${memory.src}"
            class="love-photo"
            alt="${memory.title}">
    `;

}

// ==========================================
// Create Video
// ==========================================

function createVideo(memory) {

    return `
        <video
            class="love-photo"
            controls
            autoplay
            playsinline>

            <source
                src="${memory.src}"
                type="video/mp4">

            Your browser does not support video.

        </video>
    `;

}
// ==========================================
// Create Gallery
// ==========================================

function createGallery(memory) {

    let html = `<div class="photo-collage">`;

    // Images
    if (memory.images) {

        memory.images.forEach(image => {

            html += `
                <img
                    src="${image}"
                    class="love-photo"
                    loading="lazy">
            `;

        });

    }

    // Videos
    if (memory.videos) {

        memory.videos.forEach(video => {

            html += `
                <video
    class="love-photo"
    controls
    playsinline
    preload="metadata">

    <source
        src="${video}"
        type="video/mp4">

    Your browser does not support video.

</video>
            `;

        });

    }

    html += `</div>`;

    // Audio
    if (memory.audio) {

        html += `

            <div class="memory-audio">

                <audio controls>

                    <source
                        src="${memory.audio}"
                        type="audio/mpeg">

                </audio>

            </div>

        `;

    }

    return html;

}

// ==========================================
// Typewriter Effect
// ==========================================

let typingTimer;

function typeWriter(text) {

    clearInterval(typingTimer);

    memoryMessage.innerHTML = "";

    let i = 0;

    typingTimer = setInterval(() => {

        const char = text.charAt(i);

        if (char === "\n") {

            memoryMessage.innerHTML += "<br>";

        } else {

            memoryMessage.innerHTML += char;

        }

        i++;

        if (i >= text.length) {

            clearInterval(typingTimer);

        }

    }, 18);

}

// ==========================================
// Show Memory
// ==========================================

function showMemory(index) {

    stopCurrentVideo();

    const memory = memories[index];

    mediaContainer.style.opacity = "0";

    memoryTitle.style.opacity = "0";
    memoryMessage.style.opacity = "0";

    setTimeout(() => {

        // -------------------------
        // Load Media
        // -------------------------

        switch (memory.type) {

            case "image":
                mediaContainer.innerHTML = createImage(memory);
                break;

            case "video":

    mediaContainer.innerHTML = createVideo(memory);

    const video = mediaContainer.querySelector("video");

    if(video){

        video.addEventListener("play",()=>{

            bgMusic.pause();

            musicBtn.innerHTML="▶️";

            musicBtn.classList.remove("playing");

        });

        video.addEventListener("pause",()=>{

            resumeBackgroundMusic();

        });

        video.addEventListener("ended",()=>{

            resumeBackgroundMusic();

        });

    }

    break;

            case "gallery":

    mediaContainer.innerHTML = createGallery(memory);

    const galleryVideos = mediaContainer.querySelectorAll("video");

    galleryVideos.forEach(video=>{

        video.addEventListener("play",()=>{

            bgMusic.pause();

            musicBtn.innerHTML="▶️";

            musicBtn.classList.remove("playing");

        });

        video.addEventListener("pause",()=>{

            resumeBackgroundMusic();

        });

        video.addEventListener("ended",()=>{

            resumeBackgroundMusic();

        });

    });

    break;

            default:
                mediaContainer.innerHTML = "";
        }

        // -------------------------
        // Memory Title
        // -------------------------

        memoryTitle.textContent = memory.title;

        // -------------------------
        // Typewriter Message
        // -------------------------

        typeWriter(memory.message);

        // -------------------------
        // Counter
        // -------------------------

        document.getElementById("memoryCounter").innerHTML =
            `❤️ Memory ${index + 1} of ${memories.length}`;

        // -------------------------
        // Progress Bar
        // -------------------------

        const progress =
            ((index + 1) / memories.length) * 100;

        document.getElementById("progressFill").style.width =
            progress + "%";

        // -------------------------
        // Fade In
        // -------------------------

        mediaContainer.style.opacity = "1";
        memoryTitle.style.opacity = "1";
        memoryMessage.style.opacity = "1";

        // -------------------------
        // Navigation Buttons
        // -------------------------

        prevBtn.disabled = (index === 0);

        // Keep Next button enabled
        nextBtn.disabled = false;

        // Change button text on last memory
        if (index === memories.length - 1) {

            nextBtn.innerHTML = "🥺 One Last Surprise ❤️";

        } else {

            nextBtn.innerHTML = "Next ❤️";

        }

    }, 300);

}

// ==========================================
// Next Memory
// ==========================================

function nextMemory() {

    // Last memory
    if (current === memories.length - 1) {

        openForgivePage();

        return;

    }

    current++;

    showMemory(current);

}

// ==========================================
// Open Forgive Page
// ==========================================

function openForgivePage() {

    document
        .getElementById("galleryPage")
        .classList.add("hidden");

    document
        .getElementById("forgivePage")
        .classList.remove("hidden");

    startForgiveMeter();

}

// ==========================================
// Previous Memory
// ==========================================

function previousMemory() {

    if (current <= 0)
        return;

    current--;

    showMemory(current);

}

// ==========================================
// Open Gallery
// ==========================================

function openGallery() {

    document
        .getElementById("page1")
        .classList.add("hidden");

    document
        .getElementById("galleryPage")
        .classList.remove("hidden");

    // Start background music

    bgMusic.play().then(() => {

        musicBtn.innerHTML = "⏸";

        musicBtn.classList.add("playing");

    }).catch(() => {

        console.log("Autoplay blocked.");

    });

    showMemory(current);

}

