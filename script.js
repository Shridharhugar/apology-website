// ==========================================
// Variables
// ==========================================

let current = 0;

const mediaContainer = document.getElementById("mediaContainer");
const memoryTitle = document.getElementById("memoryTitle");
const memoryMessage = document.getElementById("memoryMessage");

const nextBtn = document.getElementById("nextBtn");
const prevBtn = document.getElementById("prevBtn");
const openBtn = document.getElementById("openBtn");

const bgMusic = document.getElementById("bgMusic");

// ==========================================
// Pause Current Video
// ==========================================

function stopCurrentVideo() {

    const videos = document.querySelectorAll("video");

    videos.forEach(video => {

        video.pause();

        video.currentTime = 0;

    });

}

// ==========================================
// Create Image
// ==========================================

function createImage(memory) {

    return `
        <img
            src="${memory.src}"
            class="love-photo"
            alt="${memory.title}">
    `;

}

// ==========================================
// Create Video
// ==========================================

function createVideo(memory) {

    return `
        <video
            class="love-photo"
            controls
            autoplay>

            <source
                src="${memory.src}"
                type="video/mp4">

            Your browser does not support video.

        </video>
    `;

}

// ==========================================
// Create Gallery
// ==========================================

function createGallery(memory) {

    let html = `<div class="photo-collage">`;

    // Images
    if (memory.images) {

        memory.images.forEach(image => {

            html += `
                <img
                    src="${image}"
                    class="love-photo"
                    loading="lazy">
            `;

        });

    }

    // Videos
    if (memory.videos) {

        memory.videos.forEach(video => {

            html += `
                <video
    class="love-photo"
    controls
    playsinline
    preload="metadata">

    <source
        src="${video}"
        type="video/mp4">

    Your browser does not support video.

</video>
            `;

        });

    }

    html += `</div>`;

    // Audio
    if (memory.audio) {

        html += `

            <div class="memory-audio">

                <audio controls>

                    <source
                        src="${memory.audio}"
                        type="audio/mpeg">

                </audio>

            </div>

        `;

    }

    return html;

}

// ==========================================
// Show Memory
// ==========================================

function showMemory(index) {

    stopCurrentVideo();

    const memory = memories[index];

    mediaContainer.style.opacity = "0";

    setTimeout(() => {

        switch (memory.type) {

            case "image":

                mediaContainer.innerHTML = createImage(memory);
                break;

            case "video":

                mediaContainer.innerHTML = createVideo(memory);
                break;

            case "gallery":

                mediaContainer.innerHTML = createGallery(memory);
                break;

        }

        memoryTitle.textContent = memory.title;

        memoryMessage.textContent = memory.message;

        mediaContainer.style.opacity = "1";

        prevBtn.disabled = index === 0;

        nextBtn.disabled = index === memories.length - 1;

    }, 300);

}

// ==========================================
// Next Memory
// ==========================================

function nextMemory() {

    if (current >= memories.length - 1)
        return;

    current++;

    showMemory(current);

}

// ==========================================
// Previous Memory
// ==========================================

function previousMemory() {

    if (current <= 0)
        return;

    current--;

    showMemory(current);

}

// ==========================================
// Open Gallery
// ==========================================

function openGallery() {

    document
        .getElementById("page1")
        .classList.add("hidden");

    document
        .getElementById("galleryPage")
        .classList.remove("hidden");

    if (bgMusic) {

        bgMusic.play().catch(() => {

            console.log("Autoplay blocked.");

        });

    }

    showMemory(current);

}

// ==========================================
// Event Listeners
// ==========================================

openBtn.addEventListener("click", openGallery);

nextBtn.addEventListener("click", nextMemory);

prevBtn.addEventListener("click", previousMemory);

// ==========================================
// Keyboard Navigation
// ==========================================

document.addEventListener("keydown", (event) => {

    switch (event.key) {

        case "ArrowRight":
            nextMemory();
            break;

        case "ArrowLeft":
            previousMemory();
            break;

    }

});

// ==========================================
// Mobile Swipe Support
// ==========================================

let touchStartX = 0;
let touchEndX = 0;

document.addEventListener("touchstart", (e) => {

    touchStartX = e.changedTouches[0].screenX;

});

document.addEventListener("touchend", (e) => {

    touchEndX = e.changedTouches[0].screenX;

    if (touchStartX - touchEndX > 70) {

        nextMemory();

    }

    if (touchEndX - touchStartX > 70) {

        previousMemory();

    }

});